package fauna.repair

import fauna.atoms._
import fauna.exec.FaunaExecutionContext.Implicits.global
import fauna.lang.Timestamp
import fauna.model.Database
import fauna.repo.doc.Version
import fauna.repo.query.Query
import fauna.repo.service.StorageService
import fauna.repo.RepoContext
import fauna.storage.{ SetAction, TxnRead }
import fauna.storage.api.version.StorageVersion
import fauna.storage.lookup.{ LiveLookup, LookupEntry }
import fauna.storage.ops.LookupAdd
import fauna.tx.transaction.Partitioner
import java.io.PrintWriter
import java.lang.invoke.MethodHandles.lookup
import scala.concurrent.Future

object LookupsResolver {
  val ScopeField = Database.ScopeField
  val GlobalIDField = Database.GlobalIDField

  def apply(
    repo: RepoContext,
    storageService: StorageService,
    partitioner: => Partitioner[TxnRead, _]): LookupsResolver = {
    new LookupsResolver(
      repo,
      partitioner,
      new HostLookups(storageService, partitioner))
  }
}

final class LookupsResolver private (
  repo: RepoContext,
  partitioner: => Partitioner[TxnRead, _],
  lookupsService: HostLookups
) {

  import LookupAction._
  import LookupsResolver._

  /** This method is intended to be called with a set of lookups that were generated by
    * finding conflicts between what is in lookups and what is in versions.
    * These lookups are generated after each data exporter job by utilizing the check_lookups.py
    * script in the ops repo.  The output of the tool can be seen in the logs of each
    * data exporter run.  When this tool runs, it runs at the timestamp used for the
    * backup that it ran against. The ts provided here is that timestamp.
    */
  def resolveLookups(
    lookups: Seq[LookupData],
    snapshotTS: Timestamp,
    printWriter: PrintWriter,
    dryRun: Boolean = true): Future[Unit] = {
    if (dryRun) {
      printWriter.println("Running as dryrun so no modifications are being made.")
    }

    val startingPartitionerVersion = partitioner.version

    def resolveLookups0(
      lookups: Iterator[LookupData]
    ): Future[Unit] = {
      if (lookups.hasNext) {
        val lk = lookups.next()
        lookupsService
          .getLookup(lk, snapshotTS)
          .flatMap {
            case Left(msg) =>
              Future.successful(Decision(
                action = LookupAction.Unknown,
                None,
                s"Unexpected condition, unable to make decision for lookup=$lk msg=$msg"
              ))
            case Right(lv) =>
              lookupsService
                .getDocHistoryForLookup(lk, snapshotTS)
                .map {
                  case Left(msg) =>
                    Decision(
                      action = LookupAction.Unknown,
                      None,
                      s"Unexpected condition, unable to make decision for lookup=$lk msg=$msg"
                    )
                  case Right(None) =>
                    Decision(
                      action = LookupAction.Unknown,
                      None,
                      s"Unexpected condition, unable to make decision for $lk because no host version was found.  Because Database versions are only soft deleted this scenario is unexpected."
                    )
                  case Right(Some(version)) =>
                    processLookup(lk, lv, version)
                }
          }
          .flatMap { dec =>
            printWriter.println(dec)
            if (dryRun) {
              Future.unit
            } else {
              dec match {
                case Decision(Add, Some(lookup), _) =>
                  assert(lookup.action == SetAction.Add)
                  printWriter.println(
                    s"Encountered decision to add lookup: $lookup.  NOT taking action as add decisions requires further tooling.")
                  Future.unit
                case Decision(Remove, Some(lookup), _) =>
                  assert(lookup.action == SetAction.Remove)
                  printWriter.println(
                    s"Executing Write: logical deleting lookup with remove $lookup")
                  val wq = Query.defer {
                    if (partitioner.version != startingPartitionerVersion) {
                      throw new IllegalStateException(
                        s"Partitioner version changed from $startingPartitionerVersion to ${partitioner.version}, only execute this tool when topology is stable.")
                    } else {
                      Query.write(LookupAdd(lookup))
                    }
                  }
                  repo.result(wq).map { _ =>
                    printWriter.println(s"Successful Write: logical delete $lookup")
                  }
                case (Decision(NoAction, _, _) | Decision(Unknown, _, _)) =>
                  Future.unit
                case decision =>
                  printWriter.println(
                    s"Incomplete decision structure, if an action other than 'NoAction' is present, a lookup entry is expected: $decision")
                  Future.unit
              }
            }
          }
          .flatMap { _ => resolveLookups0(lookups) }
      } else {
        Future.unit
      }
    }

    resolveLookups0(lookups.iterator)
  }

  private def processLookup(
    lookupData: LookupData,
    lookupEntry: Option[LookupEntry],
    lookupVersion: StorageVersion
  ): Decision = {
    def lookupFromVersion(
      lookupGlobalID: GlobalID,
      version: StorageVersion): LiveLookup = {
      val versionDecoded = Version.decode(version) match {
        case v: Version.Live => v
        case _: Version.Deleted =>
          throw new IllegalStateException("Unexpected deleted version")
      }
      val globalID = {
        lookupGlobalID match {
          case _: ScopeID =>
            ScopeField
              .read(versionDecoded.data.fields)
              .toOption
              .getOrElse(
                throw new IllegalStateException(
                  s"No scope found in version ${versionDecoded.data}")
              )
          case _ =>
            versionDecoded.docID match {
              case DatabaseID(_) =>
                GlobalIDField
                  .read(versionDecoded.data.fields)
                  .toOption
                  .getOrElse(throw new IllegalStateException(
                    s"No global id found in version data ${versionDecoded.data}"))
              case KeyID(id) => GlobalKeyID(id.toLong)
              case _ =>
                throw new IllegalStateException(
                  s"Non database or key version found $versionDecoded")
            }
        }
      }
      LiveLookup(
        globalID = globalID,
        scope = version.scopeID,
        id = version.docID,
        ts = version.ts,
        action = SetAction.Add
      )
    }

    if (lookupVersion.isLive) {
      val lookupForVersion = lookupFromVersion(lookupData.globalID, lookupVersion)
      if (lookupEntry.isEmpty || lookupEntry.exists(_.isDelete)) {
        val reason =
          s"a version was found for $lookupData but no live lookup entry exists for it. LookupEntry=$lookupEntry"
        Decision(Add, Some(lookupForVersion), reason)
      } else if (lookupEntry.exists(_ != lookupForVersion)) {
        val reason =
          s"a version was found for $lookupData but the version and lookup entry don't align. Leaving data as is for further investigation." +
            s" expected=$lookupForVersion, found=$lookupEntry"
        Decision(Unknown, None, reason)
      } else {
        Decision(
          NoAction,
          None,
          s"All hosts were found to have accurate lookup: Input Data: $lookupData, Found Lookup: $lookupForVersion")
      }
    } else {
      if (lookupEntry.exists(!_.isDelete)) {
        val reason =
          s"Lookup $lookup found for deleted version $lookupVersion, adding remove for the lookup."
        Decision(
          Remove,
          Some(
            LiveLookup(
              globalID = lookupData.globalID,
              scope = lookupData.scope,
              id = lookupData.docID,
              ts = lookupVersion.ts,
              action = SetAction.Remove
            )
          ),
          reason)
      } else {
        Decision(
          NoAction,
          None,
          s"No hosts were found with incorrect lookup $lookup"
        )
      }
    }
  }
}

case class Decision(
  action: LookupAction,
  lookup: Option[LookupEntry],
  reason: String)

sealed trait LookupAction

object LookupAction {
  object Add extends LookupAction

  /** In this context a remove decision means that we will be 'adding' a LookupEntry
    * with a SetAction of remove.
    */
  object Remove extends LookupAction

  /** This signifies that upon inspection of the cluster it was determined it was
    * in the correct space for the desired lookup.
    */
  object NoAction extends LookupAction
  object Unknown extends LookupAction
}
